SELECT NAME, DATETIME
FROM ANIMAL_INS AS I
WHERE NOT EXISTS (
    SELECT 1
    FROM ANIMAL_OUTS AS O
    WHERE I.ANIMAL_ID = O.ANIMAL_ID
)
ORDER BY DATETIME
LIMIT 3









